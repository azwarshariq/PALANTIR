class userObject {
  String referenceId = '';
  String emailAddress = '';
  String firstName = '';
  String lastName = '';
  int age = 0;

  userObject(referenceId, emailAddress, firstName, lastName, age){
   this.referenceId = referenceId;
   this.emailAddress = emailAddress;
   this.firstName = firstName;
   this.lastName = lastName;
   this.age = age;
  }
}
